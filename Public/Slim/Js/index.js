function setTab(noIDName,linksID,totalNum){
for(i=1;i<=totalNum;i++){
var menu=document.getElementById(noIDName+i);
<!--对应的内容的名字-->
var con=document.getElementById(noIDName+"Con"+i);
<!--放上去的时候的样式名字-->
menu.className=i==linksID?"links2Hover":"sd002";
con.style.display=i==linksID?"block":"none";

}
}

